import { formatCounter } from './formatCounter'

it.each`
  input          | result
  ${-1610000000} | ${'-1.6B'}
  ${-100}        | ${'-100'}
  ${100}         | ${'100'}
  ${1000}        | ${'1K'}
  ${1400}        | ${'1.4K'}
  ${1410}        | ${'1.4K'}
  ${1500}        | ${'1.5K'}
  ${1510}        | ${'1.5K'}
  ${1600}        | ${'1.6K'}
  ${1610}        | ${'1.6K'}
  ${1000000}     | ${'1M'}
  ${1400000}     | ${'1.4M'}
  ${1410000}     | ${'1.4M'}
  ${1500000}     | ${'1.5M'}
  ${1510000}     | ${'1.5M'}
  ${1600000}     | ${'1.6M'}
  ${1610000}     | ${'1.6M'}
  ${1000000000}  | ${'1B'}
  ${1400000000}  | ${'1.4B'}
  ${1410000000}  | ${'1.4B'}
  ${1500000000}  | ${'1.5B'}
  ${1510000000}  | ${'1.5B'}
  ${1600000000}  | ${'1.6B'}
  ${1610000000}  | ${'1.6B'}
`('%# formatCounter($input) should be equal $result', ({ input, result }) => {
  expect(formatCounter(input)).toBe(result)
})
